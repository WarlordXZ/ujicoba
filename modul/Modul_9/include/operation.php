<?php
$hasil = NULL;

function plus($Bilangan1, $Bilangan2){

    $hasil = $Bilangan1 + $Bilangan2;
    echo "Hasil dari operasi adalah = ". $hasil;
}


function minus($Bilangan1 , $Bilangan2){

    $hasil = $Bilangan1 - $Bilangan2;
    echo "Hasil dari operasi adalah = ". $hasil;
}
function perkalian($Bilangan1 , $Bilangan2){

    $hasil = $Bilangan1 * $Bilangan2;
    echo "Hasil dari operasi adalah = ". $hasil;
}
function pembagian($Bilangan1 , $Bilangan2){

    $hasil = $Bilangan1 / $Bilangan2;
    echo "Hasil dari operasi adalah = ". $hasil;
}

?>